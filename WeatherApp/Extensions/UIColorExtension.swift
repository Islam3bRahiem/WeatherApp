import UIKit

extension UIColor {
    class var mainColor: UIColor {
        return UIColor(named: "MainColor")!
    }

    class var backgroundColor: UIColor {
        return UIColor(named: "BackgroundColor")!
    }
    
    class var separatorColor: UIColor {
        return UIColor(named: "SeparatorColor")!
    }
    
}
